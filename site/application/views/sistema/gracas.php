<h3><strong>Aliança</strong></h3>
<p><strong>Custo</strong>: 2 | <strong>Duração</strong>: 1 hora</p>
<p>O arauto cria uma reserva coletiva de pontos de fé que pode ser acessada por todos os seus aliados. A quantidade de
