<h2>Armaduras</h2>

<p>As armaduras são peças essenciais na jornada de um aventureiro, podendo ser o fator decisivo entre a vida e a morte. O primeiro aspecto a ser analisado em uma armadura é sua mobilidade, pois ela permitirá que o personagem esquive dos ataques inimigos. Essa capacidade é indicada na coluna "Des" (Destreza). Para calcular a destreza aplicável na esquiva, o jogador deve subtrair 9 da Destreza do personagem. O valor restante pode ser usado para esquivar, respeitando o limite máximo de destreza especificado pela armadura.</p>
<p>O segundo ponto importante é o valor de "G" (guarda), que representa a esquiva passiva oferecida pela armadura. Esse valor deve ser somado ao de "Des" para melhorar a esquiva. A soma dos dois valores será o seu valor de Guarda na ficha do seu personagem.</p>
<p>O terceiro fator é a proteção. Sempre que o personagem for atingido, o dano será reduzido pela proteção proporcionada pela armadura.</p>
<p>A coluna "For" (Força) indica a força mínima necessária para usar a armadura sem penalidades. Para cada ponto de força que o personagem tiver abaixo do valor mínimo exigido, ele receberá uma penalidade de 1 ponto no valor final da guarda.</p>
<p>Por último, o tamanho da armadura é crucial. Armaduras genéricas servem em qualquer biotipo, seja alto, baixo, magro ou robusto. Uma armadura sob medida, no entanto, precisa ser feita especificamente para o personagem, e ajustes têm um custo equivalente à metade do valor da armadura. Já as armaduras ajustáveis podem ser adaptadas a um novo dono por um custo baixo de 100 cobres. Quando uma armadura é encontrada, há uma chance de 1 em 6 de que ela seja do tamanho correto. O jogador escolhe um número de 1 a 6 e rola 1d6. Se o número escolhido for sorteado, a armadura é do tamanho ideal.</p>
<h3>Danos às Armaduras</h3>
<p>Durante o combate, armaduras podem sofrer avarias que gradualmente reduzem sua eficácia. Sempre que um personagem for atingido, e o resultado no d20 for menor ou igual ao valor de guarda da armadura, ele deve realizar um teste de karma. Se falhar nesse teste, a armadura será danificada em 1 ponto.</p>
<p>O dano nas armaduras é calculado da seguinte maneira: primeiro, reduz-se 1 ponto de proteção; na próxima, 1 ponto de guarda. Esse processo se repete até que tanto a guarda quanto a proteção cheguem a zero. A Loriga acolchoada sofre avaria também quando obtêm-se 1 no dado, e pode ser reparada sem custo pelo próprio personagem.</p>
<p>O custo para reparar uma armadura é equivalente a 5% de seu valor total, multiplicado pelo número de pontos degradados.</p>

