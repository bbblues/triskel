<h2>Armas</h2>

<p>Um personagem pode manusear uma arma de várias maneiras, dependendo da abordagem escolhida, o que determina o atributo utilizado. Um golpe desferido com os punhos, por exemplo, pode ser baseado na Força, Destreza ou até mesmo Sabedoria. Quando se usa a Força, o personagem confia em sua potência física, onde o impacto do soco já é suficiente para causar danos. Com a Destreza, o personagem é ágil e precisa, executando um golpe bem colocado. Já com a Sabedoria, o ataque é fruto de conhecimento, como técnicas de artes marciais. Algumas armas também permitem o uso da Astúcia, simulando uma finta para enganar o oponente, ou do Karma, quando a sorte favorece o atacante, como se o universo conspirasse a seu favor.</p>
<p>Cada arma pode ser manuseada apenas pelos atributos permitidos em sua descrição. Algumas são mais eficazes com certos atributos, oferecendo um bônus maior no ataque, tornando-as mais adequadas para determinadas classes de personagens.</p>

