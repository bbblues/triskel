<h1>Equipamentos</h1>

<p>No continente de Avengard, um elaborado sistema monetário desempenha um papel fundamental no comércio, na aquisição de bens e na economia das cidades. Esse sistema é baseado em três tipos de moeda: cobre, prata e ouro, sendo que cinquenta moedas de uma unidade equivalem a uma da próxima, seguindo essa ordem.</p>
<p>A moeda mais comum entre a população é o cobre, utilizado nas transações do dia a dia. Tão presente na rotina das pessoas, a moeda é simplesmente chamada de "cobre", sendo frequente ouvir perguntas como: "Quantos cobres isso custa?". Por isso, todos os preços são apresentados em cobre. A prata, por ser um metal mais valioso e menos abundante, é usada como símbolo de status, frequentemente vista em talheres e joias entre os mais ricos. O ouro, ainda mais raro, circula em pequena quantidade, sendo acessório de luxo exibido apenas pela elite.</p>
<p>Em termos de carga, embora as moedas sejam pequenas e ocupem pouco espaço, elas são pesadas. O jogador deve considerar que 200 moedas ocupam um volume.</p>

