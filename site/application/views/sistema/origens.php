    sendo ágil e expressiva. Essa cauda pode ajudar na comunicação de suas emoções ou intenções. Além disso, suas mãos e
    pés, embora semelhantes aos humanos, podem ter garras finas e levemente curvadas, permitindo-lhes escalar e se mover
    com agilidade. Essa adaptação física os torna habilidosos em escapar de perigos e se deslocar rapidamente pela
