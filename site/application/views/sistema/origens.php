<h2>Féarann </h2>
<p>
    Os Féarann são seres esguios e ágeis, com estaturas que variam entre a média e um pouco acima do normal. Suas peles
    têm um tom pálido, muitas vezes com um leve brilho esverdeado ou dourado, como se estivessem sempre iluminadas pela
    luz do sol filtrada através das folhas das árvores. Eles advém de um grupo de seres chamados Aes Sí que são
    imortais, entretanto os Féarann adotaram uma natureza humana.
</p>
<p>
    Os cabelos dos Féarann são longos e fluidos, apresentando cores que vão de verdes e castanhos a dourados e
    prateados, frequentemente adornados com flores silvestres, folhas ou pequenos ramos, enfatizando sua conexão com o
    ambiente natural. Seus olhos são grandes e expressivos, em tons de verde profundo, âmbar ou azul, com um brilho
    intenso que reflete tanto a sabedoria quanto a vivacidade da vida nas florestas. As feições dos Féarann são
    delicadas e harmoniosas, com traços suavemente angulares e orelhas pontiagudas que se destacam, conferindo-lhes um
    ar de mistério e beleza sobrenatural.
</p>
<p>
    Em termos de vestimenta, os Féarann usam roupas leves e práticas, feitas de tecidos naturais como linho ou algodão,
    em tons de verde, marrom e dourado. Suas roupas são projetadas para permitir liberdade de movimento, possibilitando
    que se movam rapidamente e silenciosamente entre as árvores. Muitas vezes, suas vestimentas são ornamentadas com
    padrões que imitam a flora e a fauna da floresta, reforçando sua identidade como guardiões da natureza.
</p>
