    decisões são tomadas coletivamente, com os anciãos e líderes respeitados servindo como guias e conselheiros, mas
    sempre considerando as vozes de todos os membros da comunidade. Essa estrutura social é flexível e fluida,
    permitindo que todos contribuam de acordo com suas habilidades e talentos, seja na caça, na coleta, na cura, ou na
