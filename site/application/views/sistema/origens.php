<h1>Origem</h1>
<p>
    Na antiga Tír Éireann, a Era dos Deuses marcou o auge de poder e magia, quando os seres divinos e ancestrais caminhavam
    entre os mortais, moldando o destino da terra. Os mais poderosos entre esses eram os Tuatha Dé Danann, os filhos da
    deusa Danu, que desceram sobre Tír Éireann como deuses em forma humana, trazendo consigo habilidades incomparáveis em
    magia, guerra e sabedoria. Governando com justiça e mistério, os Tuatha Dé Danann travaram grandes batalhas para
    garantir seu domínio sobre as terras férteis e místicas de Tír Éireann.
</p>
<p>
    Durante esse período, os Tuatha Dé Danann enfrentaram seus maiores inimigos, os temíveis Fomorianos, seres
    primordiais que habitavam as margens escuras e as profundezas de Tír Éireann. De aparência monstruosa e deformada, os
    Fomorianos eram uma raça de caos e destruição, que governava com uma sombra de tirania antes da chegada dos Tuatha.
    As lendas dizem que os Fomorianos possuíam tanto a força bruta quanto uma magia antiga e corrupta, o que os tornava
    adversários formidáveis. Foi na segunda batalha de Mag Tuired que os Tuatha finalmente derrotaram os Fomorianos, com
    o deus Lug matando Balor, o líder dos Fomorianos. No entanto, muitos Fomorianos não foram destruídos, e alguns até
    coexistiram com os Tuatha, tornando-se parte das lendas e do sangue misto de Tír Éireann.
</p>
<p>
    Antes mesmo da chegada dos Tuatha, Tír Éireann já havia sido habitada pelos Fir Bolg, um povo que governou a terra por um
    tempo breve, mas marcante. Embora não possuíssem a mesma conexão mágica dos Tuatha, os Fir Bolg eram guerreiros
    resilientes e ligados ao solo de Tír Éireann. Quando os Tuatha Dé Danann chegaram, as duas raças guerrearam, mas ao fim, um
    pacto de honra foi firmado, permitindo que os Fir Bolg continuassem a viver na ilha, coexistindo sob a soberania
    divina dos Tuatha.
</p>
<p>
    No entanto, com o passar das eras, os deuses perceberam que o tempo dos mortais estava por vir. Assim, os Tuatha Dé
    Danann decidiram que, embora continuassem a amar Tír Éireann, retirariam sua presença física, concedendo aos humanos o
    controle da terra. Eles se recolheram aos sídhe, colinas e montes encantados, tornando-se os Aes Sí ou Aos Sí,
    espíritos poderosos que habitam um mundo paralelo, acessível apenas em momentos especiais. Mesmo invisíveis aos
    olhos comuns, os Aos Sí ainda são reverenciados e temidos, pois seu poder nunca foi completamente apagado.
</p>
<p>
    Entre os muitos seres mágicos que persistiram em Tír Éireann após a Era dos Deuses, estão os misteriosos Púca. Criaturas
    mutantes e travessas, os Púca mantiveram seu papel como agentes de caos e capricho. Capazes de mudar de forma,
    assumindo a aparência de animais ou até de humanos, os Púca continuam vagando pelas colinas e florestas, interagindo
    com os mortais de maneira imprevisível. Em algumas noites, eles trazem boas colheitas ou presságios, mas em outras,
    arrastam os incautos para aventuras selvagens e perigosas.
</p>
<p>
    Com os deuses em seu retiro e os Aes Sí agora observando de longe, Tír Éireann entrou em uma nova era. A Era dos Homens
    havia começado, onde os mortais governariam com sua coragem, sabedoria e, ocasionalmente, com a ajuda dos antigos
    espíritos que ainda habitavam as sombras da terra. Embora os deuses não andem mais abertamente entre os homens, seu
    legado persiste nos ventos, nas colinas e nos mistérios da terra sagrada de Tír Éireann.
</p>
