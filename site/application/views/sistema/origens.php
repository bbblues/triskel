    natureza, mas também promoviam a coesão social. A conexão espiritual com a terra e a reverência por seus recursos
    naturais eram fundamentais para sua existência, acreditando que a harmonia com o ambiente era essencial para a
    prosperidade e a continuidade de sua cultura.
</p>
<p>
