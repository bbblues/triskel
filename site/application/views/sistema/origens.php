    reflete em suas práticas diárias, onde a sustentabilidade e a preservação são prioridades. Os Féarann são vistos
    como guardiões da floresta, dedicando-se a manter o equilíbrio ecológico e defendendo seu lar contra quaisquer
    ameaças.
</p>
<p>
    Os Féarann, descendentes dos Aes Sí, possuem um notável poder regenerativo que lhes permite curar ferimentos na metade do tempo habitual. Essa habilidade especial também lhes confere resistência a condições adversas, resultando em uma penalidade reduzida em um ponto para fome, sede ou fadiga, além de imunidade a venenos. Em termos de atributos, os Féarann recebem um bônus de +2 em Destreza, +1 em Sabedoria e -1 em Força, refletindo sua agilidade e conexão com a natureza.
</p>
